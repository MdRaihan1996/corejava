class FloatingPoint
{
	public static void main(String[] args) 
	{
		double[] da=new double[5];

		da[0]=2.3;
		da[1]=3.1;
		da[2]=4.1;
		da[3]=5.7;
		da[4]=8.1;

		    System.out.println(da[0]);
			System.out.println(da[1]);
			System.out.println(da[2]);
			System.out.println(da[3]);
			System.out.println(da[4]);
	}
}
